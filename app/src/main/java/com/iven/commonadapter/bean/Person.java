package com.iven.commonadapter.bean;

import java.io.Serializable;

/**
 * Created by Iven on 2016/9/12 19:42
 */
public class Person implements Serializable {
    private String name;
    private String sex;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
